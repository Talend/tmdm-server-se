package com.amalto.core.server.security;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.Collection;
import java.util.List;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

class MDMUserDetails implements UserDetails, Serializable {

    private final String userName;

    private final String password;

    private final List<GrantedAuthority> authorities;

    public MDMUserDetails(String userName, String password, List<String> roles) {
        this.userName = userName;
        this.password = password;
        // Create user authorities
        authorities = new ArrayList<GrantedAuthority>(roles.size() + 1);
        authorities.add(new SimpleGrantedAuthority(SecurityConfig.AUTHENTICATED_ROLE));
        for (String role : roles) {
            authorities.add(new SimpleGrantedAuthority(role));
        }
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return authorities;
    }

    @Override
    public String getPassword() {
        return password;
    }

    @Override
    public String getUsername() {
        return userName;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
