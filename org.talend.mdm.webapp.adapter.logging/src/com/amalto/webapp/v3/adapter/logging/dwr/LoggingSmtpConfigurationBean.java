package com.amalto.webapp.v3.adapter.logging.dwr;

public class LoggingSmtpConfigurationBean {

   private String loggingsmtpServer;
   private int loggingsmtpPort;
   private String loggingsmtpUsername;
   private String loggingsmtpPassword;
   private String loggingsmtpBCC;
   
	   
	public String getLoggingsmtpServer() {
		return loggingsmtpServer;
	}
	public void setLoggingsmtpServer(
		String loggingsmtpServer) {
		this.loggingsmtpServer = loggingsmtpServer;
	}
	public int getLoggingsmtpPort() {
		return loggingsmtpPort;
	}
	public void setLoggingsmtpPort(
		int loggingsmtpPort) {
		this.loggingsmtpPort = loggingsmtpPort;
	}
	public String getLoggingsmtpUsername() {
		return loggingsmtpUsername;
	}
	public void setLoggingsmtpUsername(
		String loggingsmtpUsername) {
		this.loggingsmtpUsername = loggingsmtpUsername;
	}
	public String getLoggingsmtpPassword() {
		return loggingsmtpPassword;
	}
	public void setLoggingsmtpPassword(
		String loggingsmtpPassword) {
		this.loggingsmtpPassword = loggingsmtpPassword;
	}
	public String getLoggingsmtpBCC() {
		return loggingsmtpBCC;
	}
	public void setLoggingsmtpBCC(
		String loggingsmtpBCC) {
		this.loggingsmtpBCC = loggingsmtpBCC;
	}
		
}
